package co.edu.udec.poo.yerlyscastellarjulio.domain;

/**
 *
 * @Yerlys Castellar
 */
public class Automotor {

    private String codigoAutomotor;
    private String planta;
    private String marca;
    private String tipo;
    private String color;

    public String getCodigoAutomotor() {
        return codigoAutomotor;
    }

    public void setCodigoAutomotor(String codigoAutomotor) {
        this.codigoAutomotor = codigoAutomotor;
    }

    public String getPlanta() {
        return planta;
    }

    public void setPlanta(String planta) {
        this.planta = planta;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

}
